console.log("Welcome to the section: Logic and Control flow");

// CONDITIONALS 

const age = 18;

if (age > 18){
    console.log('Welcome to the club !');
} else if (age === 18){
    console.log('You just turned 18, Welcome to the club !!');
} else {
    console.log('Sorry, you are not allowed to enter the club');
}


// LOOPS

// FOR LOOP
for (let i = 1; i <= 10; i++){
    console.log(i);
}

// WHILE LOOP
j = 1;

while (j <= 10){
    console.log(j);
    j++;
}